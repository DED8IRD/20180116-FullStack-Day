

def school():

    grade = int(input('What percentage did you get on your test? '))

    if grade >= 90:
        print('You got an A')

    elif grade >= 80 and grade <= 89:
        print('You got a B')

    elif grade >= 70 and grade < 79:
        print('You got a C')

    elif grade >= 60 and grade < 69:
        print('You got a D')

    else:
        print('You failed')

school()